package com.koitt.tim.dto.admin;

public class BannerDto {

    private String banner_num;
    private String banner_title;
    private String banner_image;
    private String banner_link;
    private String banner_exposure;

    public String getBanner_num() {
        return banner_num;
    }

    public void setBanner_num(String banner_num) {
        this.banner_num = banner_num;
    }

    public String getBanner_title() {
        return banner_title;
    }

    public void setBanner_title(String banner_title) {
        this.banner_title = banner_title;
    }

    public String getBanner_image() {
        return banner_image;
    }

    public void setBanner_image(String banner_image) {
        this.banner_image = banner_image;
    }

    public String getBanner_link() {
        return banner_link;
    }

    public void setBanner_link(String banner_link) {
        this.banner_link = banner_link;
    }

    public String getBanner_exposure() {
        return banner_exposure;
    }

    public void setBanner_exposure(String banner_exposure) {
        this.banner_exposure = banner_exposure;
    }
}
